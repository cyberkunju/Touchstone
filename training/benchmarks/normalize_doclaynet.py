"""
Normalize DocLayNet (COCO format) -> YOLO detection labels in the docdet-v0
12-class scheme.

DocLayNet ships COCO-style JSON: an ``images`` list (file_name, width, height,
id) and an ``annotations`` list (image_id, category_id, bbox=[x,y,w,h] in
TOP-LEFT pixel coords). We:
  1. map each annotation's category to a docdet id via class_map.DOCLAYNET_MAP,
  2. SKIP annotations whose category maps to None,
  3. convert COCO bbox (top-left x,y,w,h) -> normalized YOLO (xc,yc,w,h),
  4. clip every box to the image rectangle,
  5. write images/<split> + labels/<split> + a dataset.yaml (our 12 names) +
     a per-sample manifest (sourceType / license / split).

Pure stdlib except Pillow, which is only needed as a FALLBACK to read image
sizes when the COCO ``images`` entry lacks width/height. The Pillow import is
guarded inside the function so this file still compiles without it.

CLI:
    python normalize_doclaynet.py \
        --coco-json DocLayNet/COCO/val.json \
        --images-dir DocLayNet/PNG \
        --out benchmarks/real/doclaynet \
        --split val
"""
from __future__ import annotations

import argparse
import json
import os
import shutil
import sys

# Allow running both as a module (python -m benchmarks.normalize_doclaynet)
# and as a script (python benchmarks/normalize_doclaynet.py).
try:
    from .class_map import DOCDET_NAMES, DOCLAYNET_MAP
except ImportError:  # pragma: no cover - script-mode fallback
    sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
    from class_map import DOCDET_NAMES, DOCLAYNET_MAP  # type: ignore

DATASET_LICENSE = "CDLA-Permissive-1.0"
SOURCE_TYPE = "doclaynet"


def _image_size(img_entry: dict, img_path: str) -> tuple:
    """Return (width, height). Prefer COCO fields; fall back to Pillow."""
    w = img_entry.get("width")
    h = img_entry.get("height")
    if w and h:
        return int(w), int(h)
    # Guarded fallback — only import Pillow if we actually have to.
    try:
        from PIL import Image  # noqa: WPS433 (local import on purpose)
    except ImportError as exc:  # pragma: no cover
        raise RuntimeError(
            f"image '{img_path}' has no width/height in COCO json and Pillow "
            "is not installed to read it. pip install Pillow."
        ) from exc
    with Image.open(img_path) as im:
        return im.width, im.height


def _coco_bbox_to_yolo(bbox, img_w: int, img_h: int):
    """COCO [x,y,w,h] (top-left px) -> clipped normalized (xc,yc,w,h).

    Returns None if the clipped box has zero area.
    """
    x, y, w, h = bbox
    # Clip the box rectangle to the image first (COCO boxes can spill over).
    x1 = max(0.0, float(x))
    y1 = max(0.0, float(y))
    x2 = min(float(img_w), float(x) + float(w))
    y2 = min(float(img_h), float(y) + float(h))
    if x2 <= x1 or y2 <= y1:
        return None
    cw = x2 - x1
    ch = y2 - y1
    xc = (x1 + x2) / 2.0 / img_w
    yc = (y1 + y2) / 2.0 / img_h
    nw = cw / img_w
    nh = ch / img_h
    # Final safety clamp into [0,1].
    xc = min(1.0, max(0.0, xc))
    yc = min(1.0, max(0.0, yc))
    nw = min(1.0, max(0.0, nw))
    nh = min(1.0, max(0.0, nh))
    if nw <= 0.0 or nh <= 0.0:
        return None
    return xc, yc, nw, nh


def _write_dataset_yaml(out_root: str, split: str) -> None:
    """Write a YOLO dataset.yaml with our 12 names (idempotent merge of splits)."""
    yaml_path = os.path.join(out_root, "dataset.yaml")
    # We keep train/val/test keys pointing at our images/<split> dirs; only the
    # ones that exist will be used by ultralytics during val.
    names_block = "\n".join(f"  {i}: {n}" for i, n in enumerate(DOCDET_NAMES))
    content = (
        "# Auto-generated by normalize_doclaynet.py — docdet-v0 12-class scheme.\n"
        "# REAL benchmark set (DocLayNet). These numbers are the gate of record.\n"
        f"path: {os.path.abspath(out_root)}\n"
        "train: images/train\n"
        "val: images/val\n"
        "test: images/test\n"
        f"nc: {len(DOCDET_NAMES)}\n"
        "names:\n"
        f"{names_block}\n"
    )
    with open(yaml_path, "w", encoding="utf-8") as fh:
        fh.write(content)


def normalize(coco_json: str, images_dir: str, out_root: str, split: str,
              copy_images: bool = True) -> dict:
    """Run the conversion. Returns a stats dict."""
    with open(coco_json, "r", encoding="utf-8") as fh:
        coco = json.load(fh)

    cat_by_id = {c["id"]: c["name"] for c in coco.get("categories", [])}
    images = {img["id"]: img for img in coco.get("images", [])}

    # Group annotations by image.
    anns_by_img: dict = {}
    for ann in coco.get("annotations", []):
        anns_by_img.setdefault(ann["image_id"], []).append(ann)

    img_out = os.path.join(out_root, "images", split)
    lbl_out = os.path.join(out_root, "labels", split)
    os.makedirs(img_out, exist_ok=True)
    os.makedirs(lbl_out, exist_ok=True)

    manifest = []
    n_imgs = 0
    n_boxes = 0
    n_dropped = 0
    n_missing_img = 0

    for img_id, img_entry in images.items():
        file_name = img_entry.get("file_name")
        if not file_name:
            continue
        src_path = os.path.join(images_dir, file_name)
        if not os.path.isfile(src_path):
            n_missing_img += 1
            continue

        img_w, img_h = _image_size(img_entry, src_path)
        lines = []
        for ann in anns_by_img.get(img_id, []):
            cat_name = cat_by_id.get(ann.get("category_id"))
            docdet_id = DOCLAYNET_MAP.get(cat_name, None)
            if docdet_id is None:
                n_dropped += 1
                continue
            yolo = _coco_bbox_to_yolo(ann.get("bbox", [0, 0, 0, 0]), img_w, img_h)
            if yolo is None:
                n_dropped += 1
                continue
            xc, yc, nw, nh = yolo
            lines.append(f"{docdet_id} {xc:.6f} {yc:.6f} {nw:.6f} {nh:.6f}")
            n_boxes += 1

        base = os.path.splitext(os.path.basename(file_name))[0]
        # Write label file (even if empty — a negative sample is valid).
        with open(os.path.join(lbl_out, base + ".txt"), "w", encoding="utf-8") as fh:
            fh.write("\n".join(lines))
        # Place the image alongside (copy by default; symlink-free for Windows).
        if copy_images:
            shutil.copyfile(src_path, os.path.join(img_out, os.path.basename(file_name)))
        n_imgs += 1
        manifest.append({
            "image": os.path.basename(file_name),
            "sourceType": SOURCE_TYPE,
            "license": DATASET_LICENSE,
            "split": split,
            "width": img_w,
            "height": img_h,
            "numBoxes": len(lines),
        })

    _write_dataset_yaml(out_root, split)
    manifest_path = os.path.join(out_root, f"manifest_{split}.json")
    with open(manifest_path, "w", encoding="utf-8") as fh:
        json.dump({
            "sourceType": SOURCE_TYPE,
            "license": DATASET_LICENSE,
            "split": split,
            "samples": manifest,
        }, fh, indent=2)

    stats = {
        "out": os.path.abspath(out_root),
        "split": split,
        "images": n_imgs,
        "boxes": n_boxes,
        "droppedAnnotations": n_dropped,
        "missingImages": n_missing_img,
        "manifest": manifest_path,
    }
    return stats


def main(argv=None) -> int:
    p = argparse.ArgumentParser(description="DocLayNet COCO -> docdet-v0 YOLO labels")
    p.add_argument("--coco-json", required=True, help="DocLayNet COCO json (split file)")
    p.add_argument("--images-dir", required=True, help="dir holding the PNG/JPG images")
    p.add_argument("--out", required=True, help="output dataset root")
    p.add_argument("--split", default="val", choices=["train", "val", "test"])
    p.add_argument("--no-copy-images", action="store_true",
                   help="write labels only; do not copy images into images/<split>")
    args = p.parse_args(argv)

    stats = normalize(
        coco_json=args.coco_json,
        images_dir=args.images_dir,
        out_root=args.out,
        split=args.split,
        copy_images=not args.no_copy_images,
    )
    print(json.dumps(stats, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
